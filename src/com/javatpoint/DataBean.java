package com.javatpoint;

public class DataBean {
	
	private double std, mean;
	public DataBean()
	{
		
	}
	
	public void setStd(double value)
	{
		std=value;
	}
	public double getStd()
	{
		return std;
	}
	public void setMean(double value)
	{
		mean=value;
	}
	public double getMean()
	{
		return mean;
	}

}


